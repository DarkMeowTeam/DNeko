package dev.undefinedteam.gensh1n.utils.inv;

// Assuming IntRange is a class that represents an integer range
public class IntRange {
    private final int lower;
    private final int upper;

    public IntRange(int lower, int upper) {
        this.lower = lower;
        this.upper = upper;
    }

    public int getLower() {
        return lower;
    }

    public int getUpper() {
        return upper;
    }
}
